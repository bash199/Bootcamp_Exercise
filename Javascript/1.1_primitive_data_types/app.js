//Basics 
//data types

// a variable whose value holds a string.
let str = 'Appleseeds';
console.log(typeof(str));

// a variable whose value holds a number.
let num = 5;
console.log(typeof(num));

// a variable whose value holds an object.
let obj1 = Object(1);
console.log(typeof(obj1));
// a variable whose value holds a boolean.
let boolean = true;
console.log(typeof(boolean));

// a variable whose value holds null.
const foo = null;
console.log(typeof(foo));
// variable whose value is undefined.
let undefinedVar ;
console.log(typeof(undefinedVar));
// a variable whose value holds a symbol.
let symbol = Symbol('Apple')
console.log(typeof(symbol));